#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	int	d_i;
	int	s_i;

	d_i = 0;
	s_i = 0;
	while (dest[d_i])
	{
		d_i++;
	}
	while (src[s_i])
	{
		dest[d_i++] = src[s_i++];
	}
	dest[d_i] = '\0';
	return (dest);
}
