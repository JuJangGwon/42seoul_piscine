#include <unistd.h>

void	ft_write(char a)
{
	write(1, &a, 1);
}

void	ft_print_alphabet(void)
{
	char	c;

	c = 'a';
	while (c <= 'z')
	{
		ft_write(c);
		c++;
	}
}
